<p>El tema de la celebración del <strong><a href="https://www.cbd.int/biodiversity-day" target="_blank">Día Internacional de la Diversidad Biológica</a></strong> es <strong>“Somos parte de la solución”</strong>. El tema fue propuesto por el Convenio de Diversidad Biológica y consideramos que es de gran relevancia, y es precisamente en lo que la CONABIO ha encaminado múltiples esfuerzos por más de una década: incluir la participación de la sociedad para resolver los problemas ambientales. Por esto, desde el año pasado dejamos la Semana de la Diversidad Biológica y propusimos que celebraríamos todo un mes “Mayo, mes de la naturaleza mexicana”</p>

<p>Además, en 2021 hay por lo menos tres eventos internacionales importantes:</p>

<ul>
	<li>Se inicia la <a href="https://www.decadeonrestoration.org/es" target="_blank">Década de las Naciones Unidas de la Restauración de los Ecosistemas</a> el 5 de junio.</li>
	<li>Se inicia la <a href="https://www.oceandecade.org/" target="_blank">Década de las Naciones Unidas de las Ciencias Oceánicas para el Desarrollo Sostenible</a>.</li>
	<li>Se adopta el <strong>Marco Global de Biodiversidad Post 2020</strong> en la <a href="https://www.cbd.int/meetings/COP-15" target="_blank">15º reunión de la Conferencia de las Partes</a> en Kunming, China del 11 al 24 de octubre.</li>
</ul>

<p>La década pasada celebramos con una gran participación de la sociedad mexicana: escuelas, organizaciones de la sociedad civil, universidades, municipios, ejidos, comunidades, se sumaron a colaborar,  promoviendo el conocimiento de la riqueza natural de México a través de charlas, conferencias, videos, salidas a playas, parques, arroyos, conciertos, manualidades, etc.

<p>Para participar en esta celebración, el Convenio de Diversidad Biológica sugiere los siguientes temas:</p>
<ul>
<li>¿Qué puedo hacer para reducir mi <a href="https://www.biodiversidad.gob.mx/biodiversidad/que_hacer" target="_blank">huella ecológica?</a></li>
<li>Consume responsablemente</li>
	<ul>
	<li>Conoce y consume de prácticas sostenibles de <a href="https://www.biodiversidad.gob.mx/diversidad/alimentos/consumidor-responsable" target="_blank">agricultura, ganadería y pesca</a></li>
	<li>Conoce la situación de <a href="https://www.biodiversidad.gob.mx/diversidad/alimentos/peces/" target="_blank">peces y mariscos</a> y consume las opciones sostenibles</li>
	</ul>
<li>Promueve el <a href="https://www.biodiversidad.gob.mx/diversidad/sistemas-productivos/turismo" target="_blank">turismo sustentable</a></li> 
<li>Conoce las especies de tu <a href="https://enciclovida.mx/explora-por-region?utf8=%E2%9C%93&pagina=1#5/25.026/-83.496" target="_blank">municipio y de las áreas protegidas</a></li>
<li>Ayuda a los <a href="http://www.paismaravillas.mx/polinizadores/" target="_blank">polinizadores</a></li>
<li>Participa en <a href="https://www.biodiversidad.gob.mx/cienciaciudadana/" target="_blank">ciencia ciudadana</a></li>
</ul>

</p>
